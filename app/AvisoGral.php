<?php

namespace App;

use Illuminate\Database\Eloquent\Model;
use App\Traits\GetAllDataTrait;
use App\Traits\RelationManagerTrait;
use Illuminate\Database\Eloquent\SoftDeletes;

class AvisoGral extends Model
{
    use RelationManagerTrait,GetAllDataTrait;
    use SoftDeletes;

    public function __construct(array $attributes = array())
    {
        parent::__construct($attributes);
		$this->addRelationApp( new \App\Plantel, 'razon' );  // generated by relation command - StCliente,Cliente
		$this->addRelationApp( new \App\Puesto, 'name' );  // generated by relation command - StCliente,Cliente
    } 

	//Mass Assignment
	protected $fillable = ['desc_corta','aviso','inicio','fin','usu_alta_id','usu_mod_id', 'plantel_id', 'puesto_id'];

	public function usu_alta() {
		return $this->hasOne('App\User', 'id', 'usu_alta_id');
	}// end

	public function usu_mod() {
		return $this->hasOne('App\User', 'id', 'usu_mod_id');
	}// end

	// generated by relation command - StCliente,Cliente - start
	public function puesto() {
		return $this->belongsTo('App\Puesto');
	}// end

	// generated by relation command - StCliente,Cliente - start
	public function pivotAvisoGralEmpleado() {
		return $this->hasMany('App\PivotAvisoGralEmpleado');
	}// end

	// generated by relation command - StCliente,Cliente - start
	public function Plantel() {
		return $this->belongsTo('App\Plantel');
	}// end


    protected $dates = ['deleted_at'];
}